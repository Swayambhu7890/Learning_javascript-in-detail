//Assignment operators

let a = 5;
let b = 2;

// 1) +=
a += 4;  //a = a + 4
console.log("a = ",a); //9

// 2)-=
a -= 4;  //a = a - 4
console.log("a = ",a); //5 (as above a value is 9)

//3)*=
a *= 4;  //a = a * 4
console.log("a = ",a); //20

// 3)/= 
a /= 4;  //a = a / 4
console.log("a = ",a); //5

//4) %=
a %= 4;  //a = a % 4
console.log("a = ",a); //1

//5)**=(exponential)
a **= 4;  //a = a ** 4
console.log("a = ",a); //1  (1 raise to 4 = 1)



//Logical operators:-

// 1)&&
let a = 6;
let b = 5;

let cond1 = a>b;    //true
let cond2 = a === 6;  //true

 console.log("cond1 && cond2 = ",cond1 && cond2);//true

 // 2)||
let c = 6;
let d = 5;

let condition1 = c>d;    //true
let condition2 = c === d;  //false

 console.log("cond1 || cond2 = ",cond1 || cond2);//true (pipe||)

